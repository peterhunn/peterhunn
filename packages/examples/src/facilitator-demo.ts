/**
 * x490 facilitator demo вЂ” managed service integration.
 *
 * Shows the clean separation between open protocol and managed service:
 *
 *   Protocol (@x490/protocol):  types, middleware, ContractClient вЂ” open, self-hostable
 *   Managed service (@x490/facilitator): hosted token issuance, template registry,
 *                                         agreement tracking вЂ” the product
 *
 * Run:  npm run run:facilitator-demo
 *
 * What this shows:
 *   1. Server operator signs up в†’ gets tenantId + apiKey
 *   2. Operator registers a template в†’ gets a content-addressed hash + hosted URL
 *   3. Operator fetches ContractRequirements with facilitator endpoints pre-filled
 *   4. Operator mounts requireContract({ requirements, facilitated: true }) вЂ” no secret
 *   5. AI agent hits the gated endpoint в†’ auto-traverses в†’ 200 OK
 *   6. Operator lists agreements from the facilitator dashboard
 *   7. Operator revokes в†’ agent token rejected
 */

import { serve } from "@hono/node-server";
import { Hono } from "hono";
import {
  createFacilitatorApp,
  InMemoryTenantStore,
  InMemoryTemplateStore,
  InMemoryAgreementStore,
  FacilitatorClient,
  signUp,
} from "@x490/facilitator";
import { requireContract, ContractClient } from "@x490/protocol";
import type { ContractRequirements } from "@x490/protocol";

// в”Ђв”Ђ Start the facilitator service в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ

const FACILITATOR_PORT = 4902;
const FACILITATOR_BASE = `http://localhost:${FACILITATOR_PORT}`;

const facilitatorApp = createFacilitatorApp({
  tenants: new InMemoryTenantStore(),
  templates: new InMemoryTemplateStore(),
  agreements: new InMemoryAgreementStore(),
  baseUrl: FACILITATOR_BASE,
});

const facilitatorServer = serve({ fetch: facilitatorApp.fetch, port: FACILITATOR_PORT });
await new Promise((r) => setTimeout(r, 100));

// в”Ђв”Ђ Start the server operator's API в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ

const API_PORT = 4903;
const API_BASE = `http://localhost:${API_PORT}`;

// requirements is set after operator registration (below)
let requirements!: ContractRequirements;

const apiApp = new Hono();
apiApp.get(
  "/data",
  // facilitated: true в†’ no secret, verification delegated to facilitator
  (c, next) => requireContract({ requirements, facilitated: true })(c, next),
  (c) => c.json({ rows: 42_000, accessedBy: c.var.x490PartyId }),
);

const apiServer = serve({ fetch: apiApp.fetch, port: API_PORT });
await new Promise((r) => setTimeout(r, 100));

console.log("\nx490 facilitator demo\n");

// в”Ђв”Ђ Demo 1: Operator sign-up в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ

console.log("в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ");
console.log("  Step 1: Server operator signs up");
console.log("в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ\n");

const { tenantId, apiKey } = await signUp("Acme Data Co.", FACILITATOR_BASE);
console.log(`  tenantId: ${tenantId}`);
console.log(`  apiKey:   ${apiKey.slice(0, 18)}... (store securely вЂ” shown once)`);

const facilitator = new FacilitatorClient({ apiKey, tenantId, baseUrl: FACILITATOR_BASE });

// в”Ђв”Ђ Demo 2: Register template в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ

console.log("\nв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ");
console.log("  Step 2: Register a contract template");
console.log("в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ\n");

const ndaText = `This Data Use Non-Disclosure Agreement is entered into between Acme Data Co. (the "Discloser") and {{name}} (the "Recipient") under the laws of {{jurisdiction}}. The Recipient agrees to keep all data confidential.`;

const { hash, url } = await facilitator.uploadTemplate(ndaText, {
  title: "Data Use NDA",
  description: "Required before accessing the dataset",
});
console.log(`  template hash: ${hash.slice(0, 16)}...`);
console.log(`  hosted at:     ${url}`);

// в”Ђв”Ђ Demo 3: Build ContractRequirements в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ

console.log("\nв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ");
console.log("  Step 3: Build ContractRequirements");
console.log("в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ\n");

requirements = await facilitator.buildRequirements({
  templateHash: hash,
  requiredPartyFields: ["name", "jurisdiction"],
  resource: "/data",
  description: "Data Use NDA вЂ” required before accessing the dataset",
  expiresIn: 3600,
  negotiable: false,
});

console.log(`  acceptEndpoint: ${requirements.acceptEndpoint}`);
console.log(`  verifyEndpoint: ${requirements.verifyEndpoint}`);
console.log(`  revokeEndpoint: ${requirements.revokeEndpoint}`);
console.log(`  (no HMAC secret on the server вЂ” facilitator holds all key material)`);

// в”Ђв”Ђ Demo 4: Agent traverses the gate в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ

console.log("\nв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ");
console.log("  Step 4: AI agent accesses the gated resource");
console.log("в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ\n");

const client = new ContractClient({
  partyData: { name: "Research AI Agent", jurisdiction: "California, USA" },
  onRequirements: async (req) => {
    console.log(`  [agent] 490 received: "${req.description}"`);
    console.log(`          Fetching template from ${req.templateUrl}`);
  },
});

console.log("  в†’ GET /data (no X-490-Contract)");
const res = await client.fetch(`${API_BASE}/data`);
const body = await res.json();
console.log(`  в†ђ ${res.status}`, JSON.stringify(body));

// в”Ђв”Ђ Demo 5: Operator views agreements в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ

console.log("\nв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ");
console.log("  Step 5: Operator reviews the agreement dashboard");
console.log("в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ\n");

const agreements = await facilitator.listAgreements({ resource: "/data" });
console.log(`  ${agreements.length} agreement(s) for /data:`);
for (const a of agreements) {
  console.log(`  вЂў contractId=${a.contractId}`);
  console.log(`    party=${a.partyId}  expires=${new Date(a.expiresAt * 1000).toISOString()}`);
}

// в”Ђв”Ђ Demo 6: Revocation в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ

console.log("\nв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ");
console.log("  Step 6: Operator revokes вЂ” agent token rejected");
console.log("в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ\n");

const [agreement] = agreements;
if (agreement) {
  await facilitator.revokeAgreement(agreement.contractId, "Demo revocation");
  console.log(`  revoked contractId=${agreement.contractId}`);

  // Agent retries with the now-revoked token (client re-uses its cached token)
  console.log("\n  в†’ GET /data (cached token, now revoked)");
  const res2 = await fetch(`${API_BASE}/data`, {
    headers: { "X-490-Contract": agreement.contractId }, // contractId в‰  token, but agent has the token cached
  });
  // Use the agent's internal cache to get the token
  const cachedToken = await (client as unknown as { findCachedToken: (r: string) => string | undefined }).findCachedToken?.("/data");
  const retryHeaders: Record<string, string> = {};
  if (cachedToken) retryHeaders["X-490-Contract"] = cachedToken;

  const res3 = await fetch(`${API_BASE}/data`, { headers: retryHeaders });
  const body3 = await res3.json() as { error?: string };
  console.log(`  в†ђ ${res3.status} error="${body3.error ?? "none"}"`);
}

console.log("\nв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ\n");
facilitatorServer.close();
apiServer.close();
